#' Get logo for RStudio version
#' @param release_name RStudio release name
#' @return Character vector of logo lines
#' @keywords internal
get_rstudio_logo <- function(release_name) {
  if (release_name %in% names(rstudio_logos)) {
    return(rstudio_logos[[release_name]])
  } else {
    return(default_logo)
  }
}